class User_Input:
    def __init__(self):
        self.day = None
        self.meal = None
        
    def get_user(self):
        user_day = input("Enter 1 - for Monday, Enter 2 - for Tuesday, Enter 3 - for Wednesday, Enter 4 - for Thursday, Enter 5 - for Friday, Enter 6 - for Saturday, Enter 7 - for Sunday:")
        if user_day.isdigit():
            user_day = int(user_day)
            if user_day > 7 or user_day < 1:
                print("Enter a number between 1 and 7")
                return 
            else: 
                self.day = user_day
        else:
            print("Enter a number.")
            return
        user_meal = input("Enter 1 - for breakfast, 2 - for lunch, 3 - for dinner:")
        if user_meal.isdigit():
            user_meal = int(user_meal)
            if user_meal > 3 or user_meal < 1:
                print("Enter a number between 1 and 3")
                return
            else:
                self.meal = user_meal
        else:
            print("Enter a number.")
            return 
